<template>
    <div class="box">
        <div class="pagination_section">
            <a href="#" class="arrowLeft" @click="arrowLeft" type='link'>&laquo;</a>
            <a href="#" v-for="page, index of addPages" v-bind:key="index" class="page" @click="changeStyle">{{
                    page
            }}</a>
            <a href="#" class="arrowRight" @click="arrowRight">&raquo;</a>
        </div>
    </div>

</template>
  
<script>
export default {
    name: 'paginationList',
    methods: {
        changeStyle(event) {
            let list = Array.prototype.slice.call(document.querySelectorAll('.page'));
            list.forEach((item) => item.classList.remove('active'));
            event.target.classList.toggle('active');
            let active = document.querySelector('a.page.active');
            this.$emit('give', active);
        },
        arrowLeft() {
            let active = document.querySelector('a.page.active');
            if (active.previousSibling.classList != 'arrowLeft') {
                active.classList.remove('active')
                active.previousSibling.classList.add('active');
            }
            let change = -1;
            this.$emit('pre', change);

        },
        arrowRight() {
            let active = document.querySelector('a.page.active');
            if (active.nextSibling.classList != 'arrowRight') {
                active.classList.remove('active')
                active.nextSibling.classList.add('active');
            }
            let change = 1;
            this.$emit('pre', change);
        }
    },
    computed: {
        addPages() {
            let pages = this.$store.getters.getPages;
            for (let i = 1; i * 10 < this.$store.getters.getList.length; i++) {
                if (!pages[i]) {
                    this.$store.commit('addPages', i + 1)
                }
            }
            this.$emit('pre', 1);
            this.$emit('give', document.querySelector('a.page.active'));
            return this.$store.getters.getPages;
        }

    },
    mounted() {
        document.querySelectorAll('.page').forEach((item) => { if (item.textContent == document.querySelectorAll('.page').length) { item.classList.add('active') } });
    }
}
</script>

